function normalize(s: string): string {
  return s.normalize('NFD').replace(/[\u0300-\u036f]/g, '').toLowerCase().trim();
}

/** Búsqueda case-insensitive sin acentos, partial match. */
export function buscarComuna(query: string): Comuna[] {
  const q = normalize(query);
  if (!q) return [];
  return COMUNAS.filter((c) => normalize(c.nombre).includes(q));
}

export function comunasPorRegion(codigoRegion: string): Comuna[] {
  return COMUNAS.filter((c) => c.codigoRegion === codigoRegion);
}

export function regionDeComuna(nombreComuna: string): Region | null {
  const c = COMUNAS.find((c) => normalize(c.nombre) === normalize(nombreComuna));
  if (!c) return null;
  return REGIONES.find((r) => r.codigo === c.codigoRegion) ?? null;
}

export function getRegion(codigo: string): Region | null {
  return REGIONES.find((r) => r.codigo === codigo) ?? null;
}

